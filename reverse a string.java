import java.util.*;
public class Main
{
  public static void main (String[]args)
  {
        String str;
        Scanner sc = new Scanner(System.in);
        str = sc.nextLine();
        char[] ch = str.toCharArray();
        int n = ch.length;
        for(int i=n-1;i>=0;i--){
            System.out.print(ch[i]);
  
}
}
}
